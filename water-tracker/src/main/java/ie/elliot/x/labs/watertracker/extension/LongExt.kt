package ie.elliot.x.labs.watertracker.extension

import java.text.SimpleDateFormat
import java.util.*

fun Long.formatDate(dateFormat: String): String {
  val sdf = SimpleDateFormat(dateFormat, Locale.getDefault())

  return sdf.format(this)
}